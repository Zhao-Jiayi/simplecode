def vis(path,outpath,count):
    files = os.listdir(path)
    image_file = ""
    json_file = ""
    for f in files:
        if(os.path.isdir(path+"/"+f)):
            files.remove(f)
    file_count = len(files)
    samples = random.sample(range(file_count), count)
    print(len(samples))
    for idx in samples:
        last_name = files[idx][-3:]
        print( last_name)
        if last_name == "jpg":
            image_file = files[idx]
            json_file = files[idx][:-3] + "json"
        else:
            image_file = files[idx][:-4] + "jpg"
            json_file = files[idx]
        os.system("cp {} {}".format(path+"/"+json_file,outpath+"/"+json_file))
        json_file = path+"/"+json_file
        print(json_file)
        out_image = outpath + "/" + image_file
        with open(json_file) as f:
            data = json.load(f)
            shapes = data["shapes"]
            img = cv2.imread(path + "/" + image_file)
            cls_id = 0
            for s in shapes:
                start = (int(s["points"][0][0]),int(s["points"][0][1]))
                end = (int(s["points"][1][0]),int(s["points"][1][1]))
                color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
                text = s["label"]
                font = cv2.FONT_HERSHEY_SIMPLEX

                cv2.rectangle(img, start, end, color, 2)

         

                cv2.putText(img, text, (start[0]-5, start[1]), font, 0.75, color, thickness=2)
                cls_id+=1
            cv2.imwrite(out_image,img)
vis("../mydataset","./new",100)